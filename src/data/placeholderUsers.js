// Función para obtener estadísticas de los datos placeholder
export const getPlaceholderStats = () => {
  const total = placeholderUsers.length;
  const users = placeholderUsers.filter(u => u.role === 'user').length;
  const admins = placeholderUsers.filter(u => u.role === 'admin').length;
  const superAdmins = 0;
  const active = placeholderUsers.filter(u => u.isActive).length;
  const inactive = placeholderUsers.filter(u => !u.isActive).length;
  
  // Usuarios registrados en los últimos 30 días
  const thirtyDaysAgo = new Date();
  thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);
  const recentUsers = placeholderUsers.filter(u => u.createdAt > thirtyDaysAgo).length;
  
  return {
    totalUsers: total,
    regularUsers: users,
    totalAdmins: admins,
    totalSuperAdmins: superAdmins,
    activeUsers: active,
    inactiveUsers: inactive,
    recentRegistrations: recentUsers
  };
};

// Datos para analíticas demográficas
export const getDemographicAnalytics = () => {
  const genderStats = {
    masculino: placeholderUsers.filter(u => u.persona.sexo === 'masculino').length,
    femenino: placeholderUsers.filter(u => u.persona.sexo === 'femenino').length
  };
  
  // Distribución por edad
  const currentYear = new Date().getFullYear();
  const ageGroups = {
    '18-25': 0,
    '26-35': 0,
    '36-45': 0,
    '46+': 0
  };
  
  placeholderUsers.forEach(user => {
    const age = currentYear - new Date(user.persona.fecha_nac).getFullYear();
    if (age <= 25) ageGroups['18-25']++;
    else if (age <= 35) ageGroups['26-35']++;
    else if (age <= 45) ageGroups['36-45']++;
    else ageGroups['46+']++;
  });
  
  // Usuarios con familiar configurado
  const usersWithFamily = placeholderUsers.filter(u => u.settings.familiar_email).length;
  
  // Configuración de notificaciones
  const notificationStats = {
    enabled: placeholderUsers.filter(u => u.settings.notificar_a_familiar).length,
    silentMode: placeholderUsers.filter(u => u.settings.modo_silencio).length
  };
  
  return {
    genderStats,
    ageGroups,
    usersWithFamily,
    notificationStats
  };
};

